primerNumero = float(input('Digite el primer número: '))
segundoNumero = float(input('Digite el segundo número: '))
suma = primerNumero + segundoNumero
resta = primerNumero - segundoNumero
multiplicación = primerNumero * segundoNumero
división = primerNumero / segundoNumero
potencia = primerNumero ** segundoNumero
print('el resultado de la suma es:',suma,', de la resta:',resta,', de la multiplicación:',multiplicación,', de la división:',división,'y de la potencia es:',potencia)
